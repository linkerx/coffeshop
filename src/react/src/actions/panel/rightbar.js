export function enableRightBar() {
  return {
    type: 'RIGHTBAR_ENABLE',
  }
}

export function disableRightBar() {
  return {
    type: 'RIGHTBAR_DISABLE',
  }
}

export function toggleRightBar() {
  return {
    type: 'RIGHTBAR_TOGGLE',
  }
}

export function openRightBar() {
  return {
    type: 'RIGHTBAR_OPEN',
  }
}

export function closeRightBar() {
  return {
    type: 'RIGHTBAR_CLOSE',
  }
}
